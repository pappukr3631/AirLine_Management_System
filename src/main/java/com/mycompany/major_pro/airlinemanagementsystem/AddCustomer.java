    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String F_name = firstName.getText();
        String L_name = lastName.getText();
        String Nic = nicId.getText();
        String Passport = passportId.getText();
        String Address = address.getText();
        
        String Contact = contact.getText();
        
        String Gender = "";
        if(male.isSelected())
        {
            Gender = "Male";
        }else
        {
            Gender = "Female";
        }
        
        String CustomerId = CustomerID_JFrame.getText();
        
        DateFormat dt = new SimpleDateFormat("dd-MM-yyyy");
        String date = dt.format(dob.getDate());
        
        //Establishing Connection with the DataBase
         try {
                Class.forName("com.mysql.cj.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
        try {
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinedb", "root", "Pappukr@3631");
            
            pre = con.prepareStatement("INSERT INTO customers(CustomerID, FirstName, LastName, PassportID, NIC_ID, Address, DOB, Gender, Contact_No) VALUES(?,?,?,?,?,?,?,?,?)");
            
            pre.setString(1,CustomerId);
            pre.setString(2,F_name);
            pre.setString(3,L_name);
            pre.setString(4,Passport);
            pre.setString(5,Nic);
            pre.setString(6,Address);
            pre.setString(7,date);
            pre.setString(8,Gender);
            pre.setString(9,Contact);
            
            pre.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Customer Added");
            
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

